#include "libft.h"

char	*ft_strchr(char *s, int c)
{
	int	i;

	i = 0;
	if (!s[0] && !c)
		return (s);
	while (s[i] != '\0')
	{
		if (s[i] == c)
			return (s + i);
		if (s[i + 1] == c)
			return (s + i + 1);
		i++;
	}
	return (NULL);
}
