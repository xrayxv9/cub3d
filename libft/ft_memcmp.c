#include "libft.h"

int	ft_memcmp(char *s1, char *s2, int n)
{
	int	i;

	i = 0;
	if (n == 0)
		return (0);
	while (i < n - 1 && s1[i] == s2[i])
		i++;
	if (i >= n)
		return (0);
	return (s1[i] - s2[i]);
}
